    private void choose_display1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_choose_display1ActionPerformed
        try {
            chooser.setAcceptAllFileFilterUsed(false);
            chooser.addChoosableFileFilter(new FileNameExtensionFilter("Images", "jpg", "jpeg", "png", "gif", "bmp"));
            chooser.showOpenDialog(null);
            File file = chooser.getSelectedFile();
            
            String filePath = file.toString();
            
            inputStream1 = new FileInputStream(file);
            Path path = Paths.get(filePath);
            byte[] bytes = Files.readAllBytes(path);
            
            format = new ImageIcon(bytes);
            Image mm;
            mm = format.getImage();
            Image img2 = mm.getScaledInstance(580, 170, Image.SCALE_SMOOTH);
            ImageIcon image = new ImageIcon(img2);

            display1.setIcon(image);
            display_label.setText("Preview Display");
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(payment_modal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(featured_products_modal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_choose_display1ActionPerformed

