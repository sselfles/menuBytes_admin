    private void btn_editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_editMouseClicked
        try {
            if(inputStream1 != null){
                DatabaseConnection.getInstance().updateFeaturedProducts(inputStream1, 1);
            }
            if(inputStream2 != null){
                DatabaseConnection.getInstance().updateFeaturedProducts(inputStream2, 2);
            }
            if(inputStream3 != null){
                DatabaseConnection.getInstance().updateFeaturedProducts(inputStream3, 3);
            }
            if(inputStream4 != null){
                DatabaseConnection.getInstance().updateFeaturedProducts(inputStream4, 4);
            }
            JOptionPane.showMessageDialog(null, "Images uploaded successfully!", "Success!", JOptionPane.PLAIN_MESSAGE);
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, "Images were not uploaded properly. Please Check and upload them again.", "Something's wrong", JOptionPane.PLAIN_MESSAGE);
        }
    }//GEN-LAST:event_btn_editMouseClicked

